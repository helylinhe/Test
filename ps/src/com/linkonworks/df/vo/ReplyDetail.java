package com.linkonworks.df.vo;

public class ReplyDetail implements java.io.Serializable{

	private String id;
	private String patiSerial;
	private String inpatiNumber;
	private String replyMobileNumber;
	private String replyWay;
	private String replyContent;
	private String replyDate;
	private String name;
	
	
	
	public ReplyDetail() {
		super();
	}



	



	public ReplyDetail(String id, String patiSerial, String inpatiNumber,
			String replyMobileNumber, String replyWay, String replyContent,
			String replyDate, String name) {
		super();
		this.id = id;
		this.patiSerial = patiSerial;
		this.inpatiNumber = inpatiNumber;
		this.replyMobileNumber = replyMobileNumber;
		this.replyWay = replyWay;
		this.replyContent = replyContent;
		this.replyDate = replyDate;
		this.name = name;
	}







	public String getName() {
		return name;
	}







	public void setName(String name) {
		this.name = name;
	}







	public String getId() {
		return id;
	}



	public void setId(String id) {
		this.id = id;
	}



	public String getPatiSerial() {
		return patiSerial;
	}



	public void setPatiSerial(String patiSerial) {
		this.patiSerial = patiSerial;
	}



	public String getInpatiNumber() {
		return inpatiNumber;
	}



	public void setInpatiNumber(String inpatiNumber) {
		this.inpatiNumber = inpatiNumber;
	}



	public String getReplyMobileNumber() {
		return replyMobileNumber;
	}



	public void setReplyMobileNumber(String replyMobileNumber) {
		this.replyMobileNumber = replyMobileNumber;
	}



	public String getReplyWay() {
		return replyWay;
	}



	public void setReplyWay(String replyWay) {
		this.replyWay = replyWay;
	}



	public String getReplyContent() {
		return replyContent;
	}



	public void setReplyContent(String replyContent) {
		this.replyContent = replyContent;
	}



	public String getReplyDate() {
		return replyDate;
	}



	public void setReplyDate(String replyDate) {
		this.replyDate = replyDate;
	}
	
	
}
