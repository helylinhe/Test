package com.linkonworks.df.vo;

public class InteBusi implements java.io.Serializable{
	private String id;
	private String factoryId;
	private String busiNo;
	private String busiName;
	private String busiState;
	private String abnormalNum;
	private String callNum;
	
	/***
	 * 业务逻辑
	 * @return
	 */
	private String factoryName;
	private String state;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getFactoryId() {
		return factoryId;
	}
	public void setFactoryId(String factoryId) {
		this.factoryId = factoryId;
	}
	public String getBusiNo() {
		return busiNo;
	}
	public void setBusiNo(String busiNo) {
		this.busiNo = busiNo;
	}
	public String getBusiName() {
		return busiName;
	}
	public void setBusiName(String busiName) {
		this.busiName = busiName;
	}
	public String getBusiState() {
		return busiState;
	}
	public void setBusiState(String busiState) {
		this.busiState = busiState;
	}
	
	public String getAbnormalNum() {
		return abnormalNum;
	}
	public void setAbnormalNum(String abnormalNum) {
		this.abnormalNum = abnormalNum;
	}
	public String getCallNum() {
		return callNum;
	}
	public void setCallNum(String callNum) {
		this.callNum = callNum;
	}
	
	public String getFactoryName() {
		return factoryName;
	}
	public void setFactoryName(String factoryName) {
		this.factoryName = factoryName;
	}
	
	
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public InteBusi() {
		super();
	}
	public InteBusi(String id, String factoryId, String busiNo,
			String busiName, String busiState, String abnormalNum,
			String callNum) {
		super();
		this.id = id;
		this.factoryId = factoryId;
		this.busiNo = busiNo;
		this.busiName = busiName;
		this.busiState = busiState;
		this.abnormalNum = abnormalNum;
		this.callNum = callNum;
	}
	
	
}
