package com.linkonworks.df.vo;

public class AssistApplyRecord implements java.io.Serializable{

	private String id;
	private String consultRecordId;
	private String acceptDoctorCode;
	private String acceptDoctorMobile;
	private String patiMobile;
	private Integer replyStatus; //1已答复，0未答复
	private String replyDate;
	
	
	
	public AssistApplyRecord() {
		super();
	}



	public AssistApplyRecord(String id, String consultRecordId,
			String acceptDoctorCode, String acceptDoctorMobile,
			String patiMobile, Integer replyStatus, String replyDate) {
		super();
		this.id = id;
		this.consultRecordId = consultRecordId;
		this.acceptDoctorCode = acceptDoctorCode;
		this.acceptDoctorMobile = acceptDoctorMobile;
		this.patiMobile = patiMobile;
		this.replyStatus = replyStatus;
		this.replyDate = replyDate;
	}



	public String getId() {
		return id;
	}



	public void setId(String id) {
		this.id = id;
	}



	public String getConsultRecordId() {
		return consultRecordId;
	}



	public void setConsultRecordId(String consultRecordId) {
		this.consultRecordId = consultRecordId;
	}



	public String getAcceptDoctorCode() {
		return acceptDoctorCode;
	}



	public void setAcceptDoctorCode(String acceptDoctorCode) {
		this.acceptDoctorCode = acceptDoctorCode;
	}



	public String getAcceptDoctorMobile() {
		return acceptDoctorMobile;
	}



	public void setAcceptDoctorMobile(String acceptDoctorMobile) {
		this.acceptDoctorMobile = acceptDoctorMobile;
	}



	public String getPatiMobile() {
		return patiMobile;
	}



	public void setPatiMobile(String patiMobile) {
		this.patiMobile = patiMobile;
	}



	public Integer getReplyStatus() {
		return replyStatus;
	}



	public void setReplyStatus(Integer replyStatus) {
		this.replyStatus = replyStatus;
	}



	public String getReplyDate() {
		return replyDate;
	}



	public void setReplyDate(String replyDate) {
		this.replyDate = replyDate;
	}
	
	
	
}
