package com.linkonworks.df.vo;

public class FlupAdviseDict implements java.io.Serializable{

	private String id;
	private String evaluateName;
	private String evaluateNameSpell;
	private String evaluateContent;
	private String deptCode;
	private String evaluateBelong;
	private String logDate;
	private String logEmployeeCode;
	
	
	public FlupAdviseDict() {
		super();
	}


	public FlupAdviseDict(String id, String evaluateName,
			String evaluateNameSpell, String evaluateContent, String deptCode,
			String evaluateBelong, String logDate, String logEmployeeCode) {
		super();
		this.id = id;
		this.evaluateName = evaluateName;
		this.evaluateNameSpell = evaluateNameSpell;
		this.evaluateContent = evaluateContent;
		this.deptCode = deptCode;
		this.evaluateBelong = evaluateBelong;
		this.logDate = logDate;
		this.logEmployeeCode = logEmployeeCode;
	}


	public String getId() {
		return id;
	}


	public void setId(String id) {
		this.id = id;
	}


	public String getEvaluateName() {
		return evaluateName;
	}


	public void setEvaluateName(String evaluateName) {
		this.evaluateName = evaluateName;
	}


	public String getEvaluateNameSpell() {
		return evaluateNameSpell;
	}


	public void setEvaluateNameSpell(String evaluateNameSpell) {
		this.evaluateNameSpell = evaluateNameSpell;
	}


	public String getEvaluateContent() {
		return evaluateContent;
	}


	public void setEvaluateContent(String evaluateContent) {
		this.evaluateContent = evaluateContent;
	}


	public String getDeptCode() {
		return deptCode;
	}


	public void setDeptCode(String deptCode) {
		this.deptCode = deptCode;
	}


	public String getEvaluateBelong() {
		return evaluateBelong;
	}


	public void setEvaluateBelong(String evaluateBelong) {
		this.evaluateBelong = evaluateBelong;
	}


	public String getLogDate() {
		return logDate;
	}


	public void setLogDate(String logDate) {
		this.logDate = logDate;
	}


	public String getLogEmployeeCode() {
		return logEmployeeCode;
	}


	public void setLogEmployeeCode(String logEmployeeCode) {
		this.logEmployeeCode = logEmployeeCode;
	}


	
	
	
	
}
